  function normalize(value) {
    return String(value).replace(/\s+/g, " ").trim();
  }

  function getLanguage() {
    const urlLanguage = new URLSearchParams(window.location.search).get("lang");
    const savedLanguage = window.localStorage.getItem("virtue-language");
    const language = urlLanguage || savedLanguage || "en";
    return languages[language] ? language : "en";
  }

  function t(value, language = getLanguage()) {
    if (language === "en") return value;
    const key = normalize(value);
    return packs[language]?.[key] || value;
  }

  function translateNodeText(root, language) {
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        const parent = node.parentElement;
        if (!parent || ["SCRIPT", "STYLE", "NOSCRIPT"].includes(parent.tagName)) return NodeFilter.FILTER_REJECT;
        return normalize(node.nodeValue) ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
      },
    });

    const nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);
    nodes.forEach((node) => {
      const original = node.__virtueOriginalText || normalize(node.nodeValue);
      node.__virtueOriginalText = original;
      const translated = t(original, language);
      if (translated !== original || language === "en") {
        const leading = node.nodeValue.match(/^\s*/)?.[0] || "";
        const trailing = node.nodeValue.match(/\s*$/)?.[0] || "";
        node.nodeValue = `${leading}${translated}${trailing}`;
      }
    });
  }

  function translateAttributes(root, language) {
    root.querySelectorAll("[placeholder], [aria-label], [alt], [title]").forEach((node) => {
      ["placeholder", "aria-label", "alt", "title"].forEach((attr) => {
        if (!node.hasAttribute(attr)) return;
        const storeKey = `__virtueOriginal${attr}`;
        const original = node[storeKey] || node.getAttribute(attr);
        node[storeKey] = original;
        node.setAttribute(attr, t(original, language));
      });
    });
  }

  function languageOptions() {
    const active = getLanguage();
    return Object.entries(languages)
      .map(([code, info]) => `<option value="${code}" ${code === active ? "selected" : ""}>${info.label}</option>`)
      .join("");
  }

  function apply(language = getLanguage()) {
    const info = languages[language] || languages.en;
    document.documentElement.lang = info.lang;
    document.documentElement.dir = info.dir;
    document.body?.setAttribute("data-language", language);
    document.querySelectorAll("[data-language-select]").forEach((select) => {
      select.value = language;
    });
    translateNodeText(document.body, language);
    translateAttributes(document.body, language);
  }

  function bind() {
    document.addEventListener("change", (event) => {
      if (!event.target.matches("[data-language-select]")) return;
      window.localStorage.setItem("virtue-language", event.target.value);
      const url = new URL(window.location.href);
      url.searchParams.set("lang", event.target.value);
      window.location.href = url.toString();
    });
  }

  document.addEventListener("DOMContentLoaded", () => {
    bind();
    apply();
  });

  return { apply, getLanguage, languageOptions, t };
